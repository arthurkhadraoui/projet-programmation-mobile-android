package com.fadelkhadraoui.fadelkhadraoui;

public class Pictures {
    private int id;
    private String url;

    public Pictures(int id, String url) {
        this.id = id;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }
}
